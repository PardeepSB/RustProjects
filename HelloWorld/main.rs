//The main function is always the first code that runs in every executable Rust program.
//Good practice to place opening curly bracket on same line as the declaration, with 1 space.

//println! is a Rust macro.
//If it were a function, it would be entered as println (without !)
//the '!' just means you're calling a macro instead of normal function.
fn main() {
    println!("Hello, world!");
}
